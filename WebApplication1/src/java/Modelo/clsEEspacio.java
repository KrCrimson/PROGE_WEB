package Modelo;

/**
 *
 * @author LENOVO
 */
public class clsEEspacio {
    private int id;
    private int posicion;
    private String estado;
    private String tipo; 

    public clsEEspacio() {
    }

    public clsEEspacio(int id, int posicion, String estado, String tipo) {
        this.id = id;
        this.posicion = posicion;
        this.estado = estado;
        this.tipo = tipo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPosicion() {
        return posicion;
    }

    public void setPosicion(int posicion) {
        this.posicion = posicion;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
